    public DefaultListModel<String> setdata() throws ClassNotFoundException, SQLException {
        MyDbConnector mdc = new MyDbConnector();
        Connection con = mdc.getMyConnection();

        PreparedStatement ps = null;

        String myInsQuery = "SELECT * FROM timetable,student_course where course_Course_id=Course_id AND Student_id=? ";
        ps = con.prepareStatement(myInsQuery);
        ps.setInt(1, 1);

        ResultSet rs = ps.executeQuery();

        DefaultListModel<String> listModel = new DefaultListModel<>();

        while (rs.next()) {

            String Day = rs.getString(6);
            //System.out.println(Day);
            if ("Monday".equals(Day)) {
                System.out.println(Day);
                String Course_id = rs.getString(1);
                String Time = rs.getString(4);
                String Activity = rs.getString(5);

                listModel.addElement(Course_id);
                listModel.addElement(Time);
                listModel.addElement(Activity);

            }
        }

        return listModel;
    }

